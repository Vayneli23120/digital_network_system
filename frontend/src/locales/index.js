// 获取当前语言
export const getCurrentLang = () => {
  return localStorage.getItem('lang') || 'zh'
}

// 设置语言
export const setCurrentLang = (lang) => {
  localStorage.setItem('lang', lang)
}

// 获取翻译文本
export const t = (key) => {
  const lang = getCurrentLang()
  return languages[lang]?.[key] || languages['zh']?.[key] || key
}